package coreJava;

public class Calculator {
	
	public int add(int a, int b)
	{
		
		
		int c=a+b;
		return c;
		
	}
	public static int mul(int a, int b)
	{
		
		
		int c=a*b;
		return c;
		
	}
	public static int Sub(int a, int b)
	{
		
		
		int c=a-b;
		return c;
		
	}

}
